def generate(prompt: str, out: Path, label: str) -> bool:
    print(f"\n[{out.name}] {label}")
    body = json.dumps({
        "model": "gpt-image-2",
        "prompt": STYLE_BIBLE + "\n\n" + prompt,
        "size": "1024x1536",
        "quality": "high",
        "n": 1,
    }).encode()
    req = urllib.request.Request(
        URL, data=body,
        headers={
            "Authorization": f"Bearer {API_KEY}",
            "Content-Type": "application/json",
        },
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=300) as resp:
            result = json.loads(resp.read())
        data = result.get("data", [])
        if data and "b64_json" in data[0]:
            img = base64.b64decode(data[0]["b64_json"])
            out.write_bytes(img)
            print(f"  ✅ {len(img)//1024}KB")
            return True
        print(f"  ❌ {json.dumps(result)[:400]}")
    except urllib.error.HTTPError as e:
        print(f"  ❌ HTTP {e.code}: {e.read().decode()[:400]}")
    except Exception as e:
        print(f"  ❌ {e}")
    return False

if __name__ == "__main__":
    print("=== ameru LP 10 screens via gpt-image-1 ===")
    for fname, label, prompt in SCREENS:
        out = OUT_DIR / fname
        if out.exists() and out.stat().st_size > 30000:
            print(f"[skip] {fname} ({out.stat().st_size//1024}KB)")
            continue
        generate(prompt, out, label)
        time.sleep(2)
    print("\n=== done ===")
    for f in sorted(OUT_DIR.glob("*.png")):
        print(f"  {f.name}: {f.stat().st_size//1024}KB")
